package cn.merryyou.service.impl;

import cn.merryyou.converter.OrderMaster2OrderDTOConverter;
import cn.merryyou.dataobject.OrderDetail;
import cn.merryyou.dataobject.OrderMaster;
import cn.merryyou.dataobject.ProductInfo;
import cn.merryyou.dto.CartDTO;
import cn.merryyou.dto.OrderDTO;
import cn.merryyou.emum.OrderStatusEnus;
import cn.merryyou.emum.PayStatusEnum;
import cn.merryyou.emum.ResultEnum;
import cn.merryyou.exception.SellException;
import cn.merryyou.repository.OrderDetailRepository;
import cn.merryyou.repository.OrderMasterRepository;
import cn.merryyou.service.OrderService;
import cn.merryyou.service.ProductService;
import cn.merryyou.utils.KeyUtil;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.BeanUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.PageImpl;
import org.springframework.data.domain.Pageable;
import org.springframework.stereotype.Service;
import org.springframework.util.CollectionUtils;

import javax.transaction.Transactional;
import java.math.BigDecimal;
import java.math.BigInteger;
import java.util.List;
import java.util.stream.Collectors;

